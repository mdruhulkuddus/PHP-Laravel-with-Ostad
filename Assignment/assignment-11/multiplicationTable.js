function multiplicationTable(num) {
    for (let i = 1; i <= 10; i++) {
      console.log(`${num} x ${i} = ${num * i}`);
    }
  }
  
  // Example: generate a multiplication table for the number 7 , 9 and 15

  multiplicationTable(7);
  multiplicationTable(9);
  multiplicationTable(15);
  